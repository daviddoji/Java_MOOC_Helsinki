public class Addition {

    public static void main(String[] args) {
        // definition of varibles
        int a = 1337;
        int b = 42;
        int result = a + b;

        // parsing the result to a string
        String toPrint = a + " + " + b + " = " + result;
        // print out the result
        System.out.println(toPrint);
    }
}
